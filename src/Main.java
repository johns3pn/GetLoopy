public class Main {
    public static void main(String[] args) {

        // ---------------- TASK 7 ----------------
        System.out.println("Task 7: Square pattern");

        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}
